'use client'

import Link from 'next/link'
import { usePathname } from 'next/navigation'
import { useState } from 'react'
import clsx from 'clsx'

const NAV = [
  { href: '/dashboard',           label: 'Dashboard',   icon: IconGrid },
  { href: '/dashboard/evolucao',  label: 'Evolução',    icon: IconChart },
  { href: '/dashboard/checklists',label: 'Checklists',  icon: IconList },
  { href: '/dashboard/documentos',label: 'Documentos',  icon: IconDoc },
  { href: '/dashboard/marcos',    label: 'Marcos',      icon: IconStar },
  { href: '/dashboard/atividades',label: 'Atividades',  icon: IconActivity },
  { href: '/dashboard/chat',      label: 'Chat',        icon: IconChat },
  { href: '/dashboard/usuarios',  label: 'Usuários',    icon: IconUsers },
]

interface SidebarProps {
  criancaNome?: string
}

export function Sidebar({ criancaNome = 'Bernardo' }: SidebarProps) {
  const pathname = usePathname()
  const [open, setOpen] = useState(false)

  const isActive = (href: string) =>
    href === '/dashboard' ? pathname === href : pathname.startsWith(href)

  const sidebarContent = (
    <div className="flex flex-col h-full">
      {/* Logo / Identidade */}
      <div className="px-5 py-6 border-b border-neutral-border">
        <div className="flex items-center gap-3">
          <div className="w-9 h-9 rounded-lg bg-primary flex items-center justify-center flex-shrink-0">
            <span className="text-white font-bold text-base">M</span>
          </div>
          <div>
            <p className="text-sm font-semibold text-manolo-text leading-tight">Manolo</p>
            <p className="text-xs text-manolo-muted leading-tight">{criancaNome}</p>
          </div>
        </div>
      </div>

      {/* Navegação */}
      <nav className="flex-1 px-3 py-4 space-y-0.5 overflow-y-auto">
        {NAV.map(({ href, label, icon: Icon }) => (
          <Link
            key={href}
            href={href}
            onClick={() => setOpen(false)}
            className={clsx('sidebar-link', { active: isActive(href) })}
          >
            <Icon className="w-4 h-4 flex-shrink-0" />
            <span>{label}</span>
          </Link>
        ))}
      </nav>

      {/* Rodapé */}
      <div className="px-5 py-4 border-t border-neutral-border">
        <p className="text-xs text-manolo-muted">Fase 4 · MVP</p>
        <p className="text-xs text-manolo-muted">Sem autenticação ativa</p>
      </div>
    </div>
  )

  return (
    <>
      {/* Desktop sidebar */}
      <aside className="hidden md:flex flex-col w-56 bg-neutral-surface border-r border-neutral-border shadow-sidebar h-screen sticky top-0 flex-shrink-0">
        {sidebarContent}
      </aside>

      {/* Mobile: hamburger button */}
      <button
        className="md:hidden fixed top-4 left-4 z-50 p-2 rounded-lg bg-neutral-surface border border-neutral-border shadow-card"
        onClick={() => setOpen(true)}
        aria-label="Abrir menu"
      >
        <IconMenu className="w-5 h-5 text-manolo-text" />
      </button>

      {/* Mobile: drawer overlay */}
      {open && (
        <div
          className="md:hidden fixed inset-0 z-40 bg-black/30 backdrop-blur-sm"
          onClick={() => setOpen(false)}
        />
      )}

      {/* Mobile: drawer */}
      <aside
        className={clsx(
          'md:hidden fixed top-0 left-0 z-50 h-full w-64 bg-neutral-surface border-r border-neutral-border shadow-sidebar',
          'transform transition-transform duration-300',
          open ? 'translate-x-0' : '-translate-x-full'
        )}
      >
        <button
          className="absolute top-4 right-4 p-1 rounded-lg hover:bg-primary-50"
          onClick={() => setOpen(false)}
        >
          <IconX className="w-5 h-5 text-manolo-muted" />
        </button>
        {sidebarContent}
      </aside>
    </>
  )
}

